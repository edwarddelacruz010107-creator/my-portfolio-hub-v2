@superadmin.route('/themes/analytics')
@superadmin_required
def theme_analytics():
    entries = (
        ThemeCatalogEntry.query
        .filter(ThemeCatalogEntry.slug.in_(tuple(SUPPORTED_THEME_IDS)))
        .order_by(ThemeCatalogEntry.install_count.desc())
        .all()
    )
    data = [
        {
            'slug': e.slug,
            'name': e.name or e.slug,
            'install_count': e.install_count or 0,
            'is_featured': bool(e.is_featured),
            'is_active': bool(e.is_active),
        }
        for e in entries
    ]
    return jsonify({'ok': True, 'themes': data})



# ── Create new catalog entry ──────────────────────────────────────────────────

@superadmin.route('/themes/new', methods=['GET', 'POST'])
@superadmin_required
def theme_catalog_new():
    """
    Create a standalone catalog entry for a theme that already exists on disk.
    Useful for registering a theme that the auto-sync missed, or for pre-populating
    metadata before the filesystem theme is deployed.
    """
    if request.method == 'POST':
        slug = (request.form.get('slug') or '').strip().lower()
        name = (request.form.get('name') or '').strip()

        if not slug:
            flash('Slug is required.', 'danger')
            return render_template('superadmin/theme_new.html', page_title='New Theme Entry')

        # Validate slug against the same whitelist the theme engine uses
        from app.theme_engine import is_valid_theme_id
        if not is_valid_theme_id(slug):
            flash('Invalid slug — use only letters, numbers, hyphens, underscores.', 'danger')
            return render_template('superadmin/theme_new.html', page_title='New Theme Entry')
        if not is_supported_theme_id(slug):
            flash('Only the curated production themes can be registered.', 'warning')
            return redirect(url_for('superadmin.theme_catalog'))

        if ThemeCatalogEntry.get_by_slug(slug):
            flash(f'A catalog entry for "{slug}" already exists.', 'warning')
            return redirect(url_for('superadmin.theme_catalog'))

        entry = ThemeCatalogEntry(
            slug=slug,
            name=name or slug,
            is_active=True,
            is_premium=False,
            sort_order=0,
        )
        db.session.add(entry)
        try:
            db.session.commit()
        except Exception as exc:
            db.session.rollback()
            logger.exception('Failed to create theme catalog entry slug=%s: %s', slug, exc)
            flash('Database error while creating entry. Please try again.', 'danger')
            return render_template('superadmin/theme_new.html', page_title='New Theme Entry')

        get_theme_engine().clear_cache()
        log_activity('create', 'theme_catalog', slug, f'Created new catalog entry "{slug}"')
        flash(f'Theme entry "{slug}" created. You can now edit its metadata.', 'success')
        return redirect(url_for('superadmin.theme_catalog_edit', entry_id=entry.id))

    return render_template('superadmin/theme_new.html', page_title='New Theme Entry')


# ── Full editor (GET/POST) ────────────────────────────────────────────────────

def _get_entry_or_404(entry_id):
    entry = db.session.get(ThemeCatalogEntry, entry_id)
    if entry is None:
        abort(404)
    return entry


@superadmin.route('/themes/<int:entry_id>/edit', methods=['GET', 'POST'])
@superadmin_required
def theme_catalog_edit(entry_id):
    entry = _get_entry_or_404(entry_id)
    engine = get_theme_engine()
    fs_meta = engine.get_theme_meta(entry.slug) or {}

    if request.method == 'POST':
        form = request.form
        # ── Basic fields ──────────────────────────────────────────────────
        name          = (form.get('name') or '').strip()
        description   = (form.get('description') or '').strip()
        category      = (form.get('category') or '').strip()
        theme_author  = (form.get('theme_author') or '').strip()
        theme_version = (form.get('theme_version') or '').strip()
        required_plan = (form.get('required_plan') or '').strip().lower()
        is_premium_raw = form.get('is_premium', 'defer')
        is_active     = form.get('is_active') == 'on'
        is_featured   = form.get('is_featured') == 'on'
        thumbnail_url = (form.get('thumbnail_url') or '').strip()
        banner_url    = (form.get('banner_url') or '').strip()
        thumbnail_url, thumbnail_err = _normalize_theme_media_url(thumbnail_url)
        banner_url, banner_err = _normalize_theme_media_url(banner_url)
        try:
            sort_order = int(form.get('sort_order') or 0)
        except (TypeError, ValueError):
            sort_order = 0

        # Tags: comma-separated
        raw_tags = (form.get('theme_tags') or '').strip()
        tags = [t.strip() for t in raw_tags.split(',') if t.strip()]

        # Feature matrix: one checkbox per feature key
        _FEATURES = ['hero', 'about', 'projects', 'skills', 'services',
                     'testimonials', 'timeline', 'resume', 'contact',
                     'gallery', 'blog', 'shop']
        matrix = {f: (form.get(f'feature_{f}') == 'on') for f in _FEATURES}

        media_errors = [e for e in (thumbnail_err, banner_err) if e]
        if required_plan and required_plan not in VALID_REQUIRED_PLANS:
            flash('Invalid required plan selection.', 'danger')
        elif media_errors:
            for msg in media_errors:
                flash(msg, 'warning')
        else:
            entry.name          = name or None
            entry.description   = description or None
            entry.category      = category or None
            entry.theme_author  = theme_author or None
            entry.theme_version = theme_version or None
            entry.required_plan = required_plan or None
            entry.is_active     = is_active
            entry.is_featured   = is_featured
            entry.sort_order    = sort_order
            entry.thumbnail_url = thumbnail_url
            entry.banner_url = banner_url
            entry.set_tags(tags)
            entry.set_feature_matrix(matrix)
            if is_premium_raw == 'defer':
                entry.is_premium = None
            else:
                entry.is_premium = (is_premium_raw == 'true')

            try:
                db.session.commit()
            except Exception as exc:
                db.session.rollback()
                logger.exception('Failed to update theme catalog entry %s: %s', entry.slug, exc)
                flash('Database error while saving. Please try again.', 'danger')
                return render_template(
                    'superadmin/theme_form.html', entry=entry, fs_meta=fs_meta,
                    page_title=f'Edit Theme — {entry.slug}',
                )
            engine.clear_cache()
            log_activity('update', 'theme_catalog', entry.slug, f'Updated theme catalog entry "{entry.slug}"')
            flash(f'Theme "{entry.slug}" updated successfully.', 'success')
            return redirect(url_for('superadmin.theme_catalog'))

    return render_template(
        'superadmin/theme_form.html', entry=entry, fs_meta=fs_meta,
        page_title=f'Edit Theme — {entry.slug}',
    )


