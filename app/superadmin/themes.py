# ── Image upload config ───────────────────────────────────────────────────────

_ALLOWED_EXTS   = {'png', 'jpg', 'jpeg', 'webp', 'gif'}
_MAX_IMAGE_SIZE = 5 * 1024 * 1024  # 5 MB
_MAX_PREVIEWS   = 6                  # max screenshots per theme


def _allowed_image(filename: str) -> bool:
    return '.' in filename and filename.rsplit('.', 1)[1].lower() in _ALLOWED_EXTS


def _theme_upload_dir() -> Path:
    """Return (and create) the upload directory for theme images."""
    from app.services.media.upload_storage import ensure_upload_folder
    return ensure_upload_folder('themes')




def _normalize_theme_media_url(value: str) -> tuple[str | None, str | None]:
    """Accept only safe image URL values for theme catalog media fields.

    SuperAdmin may paste an external HTTPS/HTTP image URL or use a local
    uploaded `/uploads/...` or legacy `/static/...` path generated by this module. Rejecting every other
    scheme avoids accidentally persisting javascript:, data:, protocol-relative,
    or path-traversal values into public image attributes.
    """
    raw = (value or '').strip()
    if not raw:
        return None, None
    lower = raw.lower()
    if raw.startswith(('/static/', '/uploads/')):
        if '..' in raw or '\\' in raw:
            return None, 'Media path cannot contain traversal segments.'
        return raw, None
    if lower.startswith(('http://', 'https://')):
        return raw, None
    return None, 'Use a full https:// image URL or upload an image from this page.'

def _save_uploaded_image(file_storage, prefix: str) -> str | None:
    """Validate, optimize, and save a theme image through the active provider."""
    if not file_storage or not file_storage.filename:
        return None
    if not _allowed_image(file_storage.filename):
        return None
    # The shared helper performs MIME/magic/Pillow validation, converts to
    # WebP, and uploads to Cloudinary/Supabase/local storage as configured.
    from app.utils import save_image
    url, error = save_image(file_storage, 'themes')
    if not url:
        logger.warning('Theme image upload failed prefix=%s error=%s', prefix, error)
    return url


